import { promises as fs } from 'node:fs'
import path from 'node:path'

const rootDir = process.cwd()
const componentsDir = path.join(rootDir, 'components')
const outputFile = path.join(rootDir, 'lib', 'component-selection', 'componentHierarchy.generated.ts')

function toPascalCase(value) {
  return value
    .split(/[-_]/g)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('')
}

function parseExportNames(source) {
  const match = source.match(/export\s*\{([\s\S]*?)\}/m)
  if (!match) return []

  return match[1]
    .split(',')
    .map((raw) => raw.trim())
    .filter(Boolean)
    .map((entry) => {
      const [left] = entry.split(/\s+as\s+/)
      return left?.trim() ?? ''
    })
    .filter(Boolean)
}

async function main() {
  const entries = await fs.readdir(componentsDir, { withFileTypes: true })
  const files = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.tsx'))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b))

  const groups = []

  for (const file of files) {
    const filePath = path.join(componentsDir, file)
    const source = await fs.readFile(filePath, 'utf8')
    const exports = parseExportNames(source)
    const parentName = toPascalCase(file.replace(/\.tsx$/, ''))

    if (!exports.includes(parentName)) {
      continue
    }

    const children = exports.filter(
      (name) => name !== parentName && name.startsWith(parentName)
    )

    if (children.length === 0) {
      continue
    }

    groups.push({ name: parentName, children })
  }

  const fileContent =
    "import type { ComponentGroup } from './types'\n\n" +
    '// This file is auto-generated by scripts/generate-component-hierarchy.mjs.\n' +
    '// Do not edit manually.\n' +
    'export const componentHierarchy: ComponentGroup[] = ' +
    `${JSON.stringify(groups, null, 2)}\n`

  await fs.writeFile(outputFile, fileContent, 'utf8')
  console.log(`Generated ${path.relative(rootDir, outputFile)} with ${groups.length} groups.`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
